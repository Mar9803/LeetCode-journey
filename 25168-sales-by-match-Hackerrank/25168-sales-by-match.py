def sockMerchant(n, ar):
    pila ={}
    for i in ar:
        if i in pila:
            pila[i]+=1
        else:
            pila[i]= 1
    total_pairs =0 
    for j in pila.values():
        total_pairs += j//2
    return total_pairs
