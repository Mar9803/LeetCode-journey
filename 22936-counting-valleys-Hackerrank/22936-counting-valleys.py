def countingValleys(steps, path):
    livello_mare = 0
    valle_count = 0
    for i in path:
        if i == 'U':
            livello_mare += 1
            if livello_mare == 0:
                valle_count += 1
        elif i == 'D':
            livello_mare -= 1
            
    return valle_count

